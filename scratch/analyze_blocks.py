def analyze():
    wb = openpyxl.load_workbook(DICT_PATH, data_only=True)
    ws = wb["Translations"]
    header = [ws.cell(1, c).value for c in range(1, ws.max_column + 1)]
    col_map = {h: i + 1 for i, h in enumerate(header) if h}
    
    # Pre-parse all Excel originals for faster lookup
    excel_rows = []
    for row in range(2, ws.max_row + 1):
        val = str(ws.cell(row, col_map["Original"]).value or "")
        if val:
            excel_rows.append(val)
    
    final_json = {}
    
    for b_name, terms in RAW_BLOCKS.items():
        matches = []
        for term in terms:
            # Шукаємо термін у кожному рядку Excel
            for row_val in excel_rows:
                # 1. Розбиваємо за двократкою
                # 2. Чистимо обидві частини
                parts = row_val.split(":", 1)
                prefix = parts[0].strip()
                
                # Порівнюємо без урахування регістру (just in case) і зайвих пробілів
                if prefix.lower() == term.lower():
                    # Правило: не більше однієї двократки в усьому рядку
                    if row_val.count(":") <= 1:
                        matches.append(term)
                        break # знайшли цей термін, йдемо до наступного терміну в блоці
        
        if matches:
            final_json[b_name] = matches

    with BLOCKS_CONFIG.open("w", encoding="utf-8") as f:
        json.dump(final_json, f, ensure_ascii=False, indent=2)

    print("Blocks JSON synchronized successfully.")

if __name__ == "__main__":
    analyze()
